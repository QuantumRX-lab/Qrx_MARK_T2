const LIMIT_REACHED_TEXT = `You've reached the free limit of 8 questions for this session.

For unlimited access to the Signal Analyst, full weekly briefings, and daily Signals coverage, subscribe free at quantumrx.eu. Your session resets in 24 hours either way.`;

export default async function handler(req, res) {
  res.setHeader('Access-Control-Allow-Origin', 'https://www.quantumrx.eu');
  res.setHeader('Access-Control-Allow-Methods', 'POST, OPTIONS');
  res.setHeader('Access-Control-Allow-Headers', 'Content-Type');
  if (req.method === 'OPTIONS') return res.status(200).end();
  if (req.method !== 'POST') return res.status(405).json({ error: 'Method not allowed' });

  const ip = await logRequest(req, {
    checkBody: true,
    expectedFields: ['messages'],
  });

  const sentinelAction = await getSentinelAction(ip);
  if (sentinelAction === 'block') {
    return res.status(403).setHeader('Content-Type', 'text/html').end(warningPage(ip));
  }
  if (sentinelAction === 'honeypot') {
    return res.status(200).json({ content: [{ type: 'text', text: 'I can help you with that. What would you like to know?' }] });
  }

  // Session limit check
  const sessionCount = await getSessionCount(ip);
  if (sessionCount > SESSION_LIMIT) {
    return res.status(200).json({ content: [{ type: 'text', text: LIMIT_REACHED_TEXT }], limitReached: true });
  }

  const { messages, pageContext, storyContext, chipId } = req.body;
  if (!messages || !Array.isArray(messages)) {
    return res.status(400).json({ error: 'Invalid request' });
  }

  try {
    const apiKey = process.env.GEMINI_API_KEY_Chat;

    const lastUserMsg = [...messages].reverse().find(m => m.role === 'user');
    const lastUserText = lastUserMsg ? lastUserMsg.content : '';

    let verticals = [];
    let questionType;
    let chipGroundingStories = null;

    // A pre-grounded chip (generated fresh this morning by
    // generate-chat-chips.js) already carries its exact source stories and
    // its correct response format, decided once, by the same model that
    // phrased the question, with full context. The client only ever sends
    // the chip's id — getChipById() resolves the real grounding server-side
    // from KV, so nothing the client claims about grounding is trusted.
    const resolvedChip = chipId ? await getChipById(chipId) : null;
    if (resolvedChip) {
      questionType = resolvedChip.responseFormat;
      chipGroundingStories = resolvedChip.groundedStories;
    } else {
      verticals = detectVerticals(messages);
      const patternType = classifyQuestion(lastUserText);

      // Cross-cutting question types (comparative/speculative/synthesis) still
      // need SOME live data to reason over. If no vertical keyword matched,
      // default to the broadest feed rather than leaving them with nothing
      // but the weekly briefing — this closes the gap where a question like
      // "who's closest to cracking fusion" got zero live context at all.
      if (!verticals.length && ['comparative', 'speculative', 'synthesis'].includes(patternType)) {
        verticals = ['hot'];
      }

      // storyContext (a tapped story) always forces single_story regardless
      // of question wording, matching the note appended in buildSystemPrompt.
      questionType = storyContext
        ? 'single_story'
        : (patternType || (verticals.length ? 'single_story' : 'general'));
    }

    const [briefing, signalsContext] = await Promise.all([
      getWeeklyBriefing(),
      // No need to hit the live Signals KV feed at all for a pre-grounded
      // chip — the exact stories it needs are already attached.
      chipGroundingStories ? Promise.resolve({}) : getSignalsContext(verticals),
    ]);

    const systemPrompt = buildSystemPrompt(briefing, signalsContext, pageContext || null, storyContext || null, questionType, chipGroundingStories);

    const contents = messages.map(m => ({
      role: m.role === 'assistant' ? 'model' : 'user',
      parts: [{ text: m.content }]
    }));

    const response = await fetch(
      `https://generativelanguage.googleapis.com/v1beta/models/gemini-2.5-flash:generateContent?key=${apiKey}`,
      {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({
          system_instruction: { parts: [{ text: systemPrompt }] },
          contents,
          generationConfig: { maxOutputTokens: 2048, temperature: 0.7 }
        })
      }
    );
    const data = await response.json();
    if (!response.ok) {
      console.error('Gemini error:', data);
      return res.status(500).json({ error: 'Gemini API error' });
    }
    const text = data.candidates?.[0]?.content?.parts?.[0]?.text || 'No response generated.';
    const parsed = parseResponseByType(text, questionType);

    // readLink is derived here, not parsed from the model's output. For a
    // pre-grounded chip, use the vertical it was actually generated from;
    // otherwise, whichever vertical was fetched for a free-typed question.
    const readLink = chipGroundingStories
      ? (chipGroundingStories[0]?.vertical || null)
      : (verticals.length ? verticals[0] : null);

    return res.status(200).json({
      content: [{ type: 'text', text }], // raw text retained for backward compatibility / debugging
      responseFormat: questionType,
      title: parsed.title,
      sections: parsed.sections,
      readLink,
      sessionCount,
      sessionLimit: SESSION_LIMIT,
      weekLabel: briefing?.weekLabel || null,
      storyCount: briefing?.stories?.length || 0,
    });
  } catch (err) {
    console.error('Proxy error:', err);
    return res.status(500).json({ error: 'Internal server error' });
  }
}
